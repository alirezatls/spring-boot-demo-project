package com.demo.rest.ajax.restdemoajax.dto;

public class HumanDto {
    private int id;
    private String name;
    private String family;

    public HumanDto() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFamily() {
        return family;
    }

    public void setFamily(String family) {
        this.family = family;
    }
}
